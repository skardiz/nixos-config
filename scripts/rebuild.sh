#!/usr/bin/env bash
# Завершить скрипт, если любая команда завершится с ошибкой
set -e

# Переходим в директорию с конфигурацией
# Убедитесь, что этот путь правильный для вашей системы
cd /home/alex/nixos-config || exit

# --- Шаг 1: Обновление зависимостей ---
echo -e "\n\e[32m>>> Шаг 1: Обновление зависимостей (flake update)...\e[0m"
nix flake update

# --- Проверка на наличие изменений ---
# Если нет изменений, просто пересобираем систему и выходим
if [ -z "$(git status --porcelain)" ]; then
  echo -e "\n\e[33m>>> Нет изменений для коммита. Запускаем только сборку системы...\e[0m"
  sudo nixos-rebuild switch --flake .#shershulya
  echo -e "\n\e[1;32m✅ Система успешно пересобрана (без новых коммитов)!\e[0m"
  exit 0
fi

# Если изменения есть, требуем сообщение для коммита1
if [ -z "$1" ]; then
  echo "Ошибка: есть изменения, но не указано сообщение для коммита."
  echo "Пример: rebuild \"feat: добавил новый пакет\""
  exit 1
fi

# --- Шаг 2: Тестовая сборка системы ---
# Это самый важный шаг. Мы пытаемся собрать новую конфигурацию, но НЕ активируем ее.
# Если на этом шаге произойдет ошибка, скрипт прервется ДО создания коммита.
echo -e "\n\e[32m>>> Шаг 2: Тестовая сборка системы (nixos-rebuild build)...\e[0m"
sudo nixos-rebuild build --flake .#shershulya
echo -e "\e[32m>>> Сборка прошла успешно!\e[0m"

# --- Шаг 3: Добавление, коммит и отправка изменений ---
# Этот блок выполняется, только если тестовая сборка на Шаге 2 была успешной.
echo -e "\n\e[32m>>> Шаг 3: Добавление всех изменений в Git...\e[0m"
git add .

echo -e "\n\e[32m>>> Шаг 4: Создание коммита...\e[0m"
git commit -m "$1"

echo -e "\n\e[32m>>> Шаг 5: Отправка изменений на GitHub...\e[0m"
git push

# --- Шаг 6: Активация уже собранной конфигурации ---
# Теперь мы активируем конфигурацию. Эта команда выполнится очень быстро,
# так как система уже была собрана на Шаге 2.
echo -e "\n\e[32m>>> Шаг 6: Активация новой конфигурации (nixos-rebuild switch)...\e[0m"
sudo nixos-rebuild switch --flake .#shershulya

echo -e "\n\e[1;32m✅ Система успешно обновлена, и изменения отправлены на GitHub!\e[0m"
